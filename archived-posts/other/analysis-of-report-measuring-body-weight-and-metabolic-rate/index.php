                          <![endif]>
                        </span></span></a>
                    This can be measured with a few different methods; the most accurate is
                    indirect calorimetry.<span style='mso-spacerun:yes'>  </span>Easier, but less
                    accurate is the use of a device such as the <span class=SpellE>BodyGem</span>,
                    and the easiest of all is to use an equation, such as the Weir equation.<a
                      style='mso-footnote-id:ftn2' href="#_ftn2" name="_ftnref2" title=""><span
                        class=MsoFootnoteReference><span style='mso-special-character:footnote'>
                          <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;
