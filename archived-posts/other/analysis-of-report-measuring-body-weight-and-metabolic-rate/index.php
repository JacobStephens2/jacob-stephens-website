                <p class=MsoNormal style='line-height:200%'><b style='mso-bidi-font-weight:
normal'><span style='font-family:"Times New Roman",serif'>Introduction<o:p></o:p></span></b></p>

                <p class=MsoNormal style='line-height:200%'><span style='font-family:"Times New Roman",serif'>The
                    purpose of this study is to evaluate the validity of a study involving men,
                    women, weight, and metabolic rate. I will calculate regression of the
                    relationships between these variables. I will also ask what type of
                    relationship these variables have, linear, quadratic? Also, what is the best
                    type of equation to model these variables with, one with multiple or a single
                    variable?<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><b style='mso-bidi-font-weight:
normal'><span style='font-family:"Times New Roman",serif'>Data Collection<o:p></o:p></span></b></p>

                <p class=MsoNormal style='line-height:200%'><span style='font-family:"Times New Roman",serif'>Metabolic
                    rate is a measurement that reports kilocalories burned per 24 hours.<a style='mso-footnote-id:ftn1'
                      href="#_ftn1" name="_ftnref1" title=""><span class=MsoFootnoteReference><span
