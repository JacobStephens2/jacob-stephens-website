                <p class=MsoNormal style='line-height:200%'><i style='mso-bidi-font-style:normal'><span
                      style='font-family:"Times New Roman",serif'>1.</span></i><span
                    style='font-family:"Times New Roman",serif'>
                    <o:p></o:p>
                  </span></p>

                <p class=MsoNormal style='line-height:200%'><span style='font-family:"Times New Roman",serif'>The
                    results may be affected by the fact that the subjects were dieting. Certain
                    subjects may shift in metabolic rate as a reaction to the changed diet more
                    than other subjects. This could create lower correlation in data by having a
                    spread of subjects, some of which fluctuate more than others in metabolic rate
                    as a reaction the changed diet. <o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><i style='mso-bidi-font-style:normal'><span
                      style='font-family:"Times New Roman",serif'>2.</span></i><span
