                <p class=MsoNormal style='line-height:200%'><span style='font-family:"Times New Roman",serif'>The
                    three conditions for inference are normal residuals, linearity, and constant
                    standard deviation.<a style='mso-footnote-id:ftn3' href="#_ftn3" name="_ftnref3" title=""><span
                        class=MsoFootnoteReference><span style='mso-special-character:
footnote'>
                          <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;mso-fareast-font-family:
