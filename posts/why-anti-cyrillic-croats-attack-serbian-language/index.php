          <article class="single-module--article--1iUKO">
            <h1 class="single-module--article__title--1m8hL">Why anti-Cyrillic Croats attack Serbian Language</h1>
            <div class="single-module--article__meta--2B_eP">by <!-- -->Jacob Stephens<!-- -->. Published<!-- -->
              <!-- -->December 18, 2015</div>
            <div class="single-module--article__content--7dEpL">
              <div class=WordSection1>

                <p class=MsoNormal style='line-height:200%;mso-outline-level:1'><b
                    style='mso-bidi-font-weight:normal'><span
                      style='mso-bidi-font-family:"Times New Roman"'>Introduction</span></b><span
                    style='mso-bidi-font-family:"Times New Roman"'>
                    <o:p></o:p>
                  </span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>My question: how can we understand the violence of the
                    anti-Cyrillic group against their Serb counterparts?<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Presently in Croatia, tension exists in Croatia over law
                    requiring Serbian language in public spaces. After the 2011 Croatian census, <span class=GramE>a
                      number of</span> cities in the Croatian county of <span class=SpellE>Vukovar-Syrmia</span>
                    realized that they would be legally bound to
                    erect bilingual signs featuring both the Latin script Croatian and the Cyrillic
                    script Serbian. The two languages are nearly as similar spoken as American and
                    Australian English. In 2013, after the town of <span class=SpellE>Vukovar</span>
                    placed bilingual signs at the entrance of the town hall, a mob of war vets who
                    had defended the city gathered and violently tore down the signs. Resulting<b
                      style='mso-bidi-font-weight:normal'> </b>scuffles<b style='mso-bidi-font-weight:
normal'> </b>between the protestors and police injured people. That same year,
                    20,000 people gathered in the Croatian capital protesting the incorporation of
                    Cyrillic into public use (Pavelic). Minority language is required in public use
                    in districts where over a third of the population is a minority. <span class=GramE>In order
                      to</span> request a change to this law, 10% of the voting
                    population must sign a petition, which in turn authorizes a referendum. In
                    Croatia that&#8217;s about 450,000 Croatian signatures required. In 2014, an
                    anti-Cyrillic group gathered a petition of over 575,000 signatures requesting a
                    change to the Constitutional Law on National Minorities. The petition requested
                    an increase in the population requirement for official minority language use
                    from one third to one half.<span style='mso-spacerun:yes'>  </span>We see a
                    significant portion of Croatia who support the exclusion of Cyrillic from
                    public space.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Through the course of this paper, I will first describe
                    Girard&#8217;s theory of mimetic desire, his theory of sacrifice, and finally explain
                    why the tension over Serbian exists in Croatia using Girard&#8217;s theory.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>My thesis: Anti-Cyrillic Croats commit violence because they
                    desire pure identity, but they cannot acquire pure identity because of the complicit
                    ways they were involved in the war. <span class=GramE>In order to</span>
                    reunite their fraying sense of togetherness they gather against Serbs. In
                    pursuit of their competing <span class=GramE>desires</span> they do violence to
                    each other. They find each other as<b style='mso-bidi-font-weight:normal'> </b>obstacles
                    on their respective paths to acquire their object of desire, which is pure
                    identity. This is a situation of mimetic rivalry. They want to alleviate these <span
                      class=GramE>tensions, and</span> have done so by sacrifice. That gives them a
                    renewed sense of unity as they now have a <span class=GramE>goal</span> they
                    can all take part in, attacking the Serbs. For the sake of simplicity, we will
                    refer to the anti-Cyrillic group as Croats, however, there are pro-Cyrillic
                    Croats. Croats mythologize the invasion of <span class=SpellE>Vukovar</span>, victimize
                    Serbs, then get the scapegoat effect. Understanding the inter and intra group
                    relations in this situation helps us determine how to resolve the violence that
                    is happening. <o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%;mso-outline-level:1'><b
                    style='mso-bidi-font-weight:normal'><span
                      style='mso-bidi-font-family:"Times New Roman"'>Girard&#8217;s
                      mimetic theory</span></b><span style='mso-bidi-font-family:"Times New Roman"'>
                    <o:p></o:p>
                  </span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Girard has a theory of mimetic desire, which explains the
                    ways that people develop desire. People develop desire by influencing one
                    another. In Williams&#8217; <i style='mso-bidi-font-style:normal'>The Girard Reader</i>,
                    on page 9, Girard states, &quot;If acquisition and appropriation were included
                    [in the literature on imitation], imitation as a social phenomenon would turn
                    out to be more problematic than it appears, and above all conflictual.&quot;
                    This is a type of philosophical anthropology. Girard <span class=GramE>offers an
                      explanation for</span> how peoples&#8217; desires originate. If party A desires
                    national identity, and party A interacts with party B, party B may develop the
                    same desire for a national identity.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Mimetic rivalry is the situation when multiple parties are
                    fighting over the same object of desire, but they cannot both acquire or attain
                    it. Girard states that &quot;Each tries to push aside the obstacle that the
                    other places in his path. Violence is generated by this process; or rather
                    violence is the process itself when two or more partners try to prevent one
                    another from appropriating the object they all desire through physical or other
                    means.&quot; As multiple individuals, such as various war veterans within the
                    Croatian war veteran community, strive for the same object, such as a unique
                    military honor, violence is when they take efforts to stop or block the other
                    from acquiring the military honor, so that they themselves can have it instead.
                    Mimetic rivalry occurs over objects that both parties cannot acquire. There is
                    only enough for one or one group.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Further, Girard goes on to <span class=GramE>offer an
                      explanation of</span> ritual using mimetic rivalry. Religions create
                    prohibitions <span class=GramE>in order to</span> limit the conflicts that
                    could come up as a result of the ways people push each other out of the way. Monogamous
                    marriage is a Christian ritual. A ritual is a way that people repeatedly act, and
                    imbue those acts with meaning, and may even be believed to enact change. The
                    ritual act may be understood as more than symbolic, but as sacramental &#8211;
                    effecting substantive <span class=GramE>change in reality</span>. But how could
                    monogamous marriage be limiting in a helpful way, a prohibition that limits
                    mimetic conflict? This ritual may have begun out of a community where there was
                    a lack of order, or a lack of differentiation. When people are undifferentiated
                    and vying for the same limited objects, they end up conflicting. Not everyone
                    can have the one <span class=GramE>particular person</span> as a spouse, but
                    when the ritual of marriage is enacted, those two become a distinct unit &#8211; <i
                      style='mso-bidi-font-style:normal'>differentiated</i> from others. This
                    establishment creates a clear boundary between people as to what objects they
                    may desire, and this helps minimize people from desiring the same person as a
                    spouse. When someone is married, it becomes taboo to want that person for <span
                      class=GramE>one&#8217;s self</span>. These sorts of prohibitions discourage mimetic
                    rivalry.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Mimetic theory explains myths, which often are told in
                    tandem with rituals. Girard explains the structure of myth. The community is
                    disordered or undifferentiated. A mimetic disturbance in the community
                    generates the myth. An individual or group is convicted of a fault and then that
                    same individual seen as the cause of the crisis. The victim is killed. Then
                    order is regenerated, and the victim is revealed as divine or sacred. <o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Sacrifice occurs at the end of some rituals, and this plays
                    a key, unifying function in a group. &quot;Real or symbolic, sacrifice is
                    primarily a collective action of the entire community, which purifies itself of
                    its own disorder through the unanimous immolation of a victim, but this can
                    happen only at the paroxysm of the ritual crisis.&quot; After the violence of
                    mimetic rivalry, people often feel chaotic, and disparate in their sense of
                    group identity. Once they create a rival, then they have again a sense of
                    unification of purpose &#8211; to remove the threat, and order is temporarily
                    restored.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%;mso-outline-level:1'><b
                    style='mso-bidi-font-weight:normal'><span style='mso-bidi-font-family:"Times New Roman"'>The
                      development of Croatian desire for pure identity</span></b><span
                    style='mso-bidi-font-family:"Times New Roman"'>
                    <o:p></o:p>
                  </span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>As Misha Glenny observes in his account of the beginning of
                    the Balkan Wars of the 90s, the leaders of Bosnia and Macedonia recognized that
                    if Croatia were to secede from Yugoslavia, they would need to become
                    &quot;identified explicitly with the Croat people to the exclusion of others&quot;.
                    This is one recognition that shows that during the war, Croatia painted itself
                    as a nation primarily of Croats. Given this emphasis on being a Croatian, that
                    same sentiment extended into the army, and soldiers received that
                    encouragement. Now presently, as veterans continue to hold on to the identity
                    given to them during war time, in order to maintain <span class=GramE>that,</span>
                    there needs to be substantive differentiation between them and Serbs, just as
                    there was in the war. During wartime, Serbs were the enemies. Now, a
                    differentiation between Croats and Serbs is that Croats get to use their
                    language in the public space, excluding Serbian. If Serbian becomes an official
                    language as well, a level of distinction and differentiation is lost between
                    the Croats and Serbs. The Croats desire became, <span class=GramE>as a result
                      of</span> the messages governments sent their people: I want to be a good
                    Croatian, which meant hating Serbs.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Many Croats in post-war Croatia have desired a pure
                    identity, especially veterans who were told they were the righteous people
                    going in to fight the dirty Serb <span class=SpellE>Ćetniks</span>. The term <span
                      class=SpellE>Ćetnik</span> has history in World War II. During that time there
                    was a political group fighting for revolution who were called <span class=SpellE>Ćetniks</span>.
                    They were a very violent group who slaughtered
                    many Croatians. And as some people started to desire pure identity, others
                    followed suit. The Tudjman administration portrayed a nationalistic image of
                    the good Croatian fighters who avenge wrongs done in the distant past to the
                    Croatian people. In 1991 Croatia persuaded Germany &quot;that its cause was
                    just by presenting itself &#8216;as an integral part of a civilized Catholic, central
                    European culture while denigrating its Serbian neighbor as a representative of
                    the barbaric, despotic Orient. Moreover, the drive for independence was
                    presented as an act of liberation from decades of Serbian oppression.&quot; In
                    this way, the veterans were portrayed as a type of hero fighting for freedom
                    and the purity of Croatia. As veterans came back, they retained this desire to
                    be a pure people, and others caught on and mimicked their desire.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Why do the Croatian veterans want to guard their pure
                    identity so much? It may be one of the few pieces of their identity that gives
                    them a greater sense of meaning or purpose. They have returned to society, to a
                    life that is not quite as intense or purposeful as their life in war was. As Chris
                    Hedges observes, war &quot;does create a feeling of comradeship that
                    obliterates<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>our alienation and makes us, for perhaps the only time in
                    our life, feel that we belong. War allows us to rise above our small stations
                    in life. We find nobility in a cause and feelings of selflessness, even bliss.
                    At a time of soaring deficits and financial scandals and the very deterioration
                    of our domestic fabric, war is a fine diversion.&quot; If that experience of
                    nobility during war becomes a memory that is marred and impure, they may be
                    devastated as a result.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%;mso-outline-level:1'><b
                    style='mso-bidi-font-weight:normal'><span style='mso-bidi-font-family:"Times New Roman"'>Fraying
                      of unity in the Croatian veteran community</span></b><span style='mso-bidi-font-family:
"Times New Roman"'>
                    <o:p></o:p>
                  </span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>People clashed though as they pursued this object of desire,
                    this purity of identity, which is a desire to have one&#8217;s war identity be
                    unmarred and clean, not <span class=GramE>dirty</span> or wrong. People want
                    that identity to be them having fought for good, just as the government told
                    them they were doing &#8211; fighting for justice. Unfortunately, they could not and would
                    never attain this <span class=GramE>object</span> but they did not recognize
                    that, but, in keeping hope that they can, so they pushed the others out of the
                    way in their pursuit, doing violence to each other in the process. They
                    believed that they needed this pure identity to be happy and satisfied.
                    Croatian war veterans experienced a tension with this message about who they
                    are while at war, where they committed war crimes and lost some of their purity.
                    Croats carried out operations where Serbian civilian areas were the targets for
                    bombing (Traynor). These operations have been judged as acts of ethnic
                    cleansing. These memories are suppressed and hidden, however, and never brought
                    up by the anti-Cyrillic Croats, so that they can maintain the illusion that
                    they were capable of being and becoming the pure people they were told they
                    were. Croats began to get anxious as they go longer without acquiring their
                    object of desire, and they end up hurting other members of the same-desiring community
                    perceived as obstacles to getting what they need. PTSD in Croatia is shown as a
                    prevalent phenomenon, especially as causing poorer quality of life for family
                    members of veterans with PTSD (Peraica et. al.). In these families there is
                    shown to be damage to psychological health and social relations. A women&#8217;s
                    rights organization called Be active, <span class=GramE>Be</span> emancipated (<span
                      class=SpellE>B.a.B.e</span>.), has programs dedicated to counseling for healing
                    from domestic abuses, and legal advice and assistance for follow up on abuses.
                    We see that violence has festered within the Croat community itself, and I
                    suggest in the entire pure-identity desiring community.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Why would war veteran Croats keep each other from reaching
                    their goal of pure identity? As vets remembered their war deeds, others cringed
                    because of the dirtiness of them, which revealed to them the dirtiness, if they
                    abuse others in response to their disgusted reactions, perhaps that will quiet
                    them, and then they can stop having their pure war hero image marred by others.
                    Others kept war vets from having a pure identity. If they bring this part of
                    their identity out and discuss it others will be disgusted. Also, others may
                    bring up and critique the actions that the Croats took during the war. The
                    criminality of concentration camps is one thing that may be brought up, which
                    results in vets being reminded of the bad things they did. They cannot fool
                    themselves into being pure and blameless when they are having their war crimes
                    brought to their attention. Then these reactions and messages get in the way of
                    the veterans attaining what they believe will satisfy them. So perhaps abuse
                    would silence those who are bringing up these messages. The Croat war vet
                    community not only builds tensions <span class=GramE>as a result of</span>
                    attaining their goal continually being thwarted, but it also begins to be disordered
                    and chaotic. Abuses in families and lack of social relationships give less of a
                    sense of cohesion to the community.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%;mso-outline-level:1'><b
                    style='mso-bidi-font-weight:normal'><span style='mso-bidi-font-family:"Times New Roman"'>Veterans
                      unify against Serbs</span></b><span style='mso-bidi-font-family:"Times New Roman"'>
                    <o:p></o:p>
                  </span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Croats need a solution for the tensions building up in their
                    community. They must find some way to bring themselves back together while they
                    continue pursuing their object of desire. <span class=GramE>In order to</span>
                    draw the community together without doing too much damage to another member, or
                    without having to admit their own fault in the problem, they find a party who
                    can take the blame for the ills and problems the community is facing. In the
                    case of the Croat, they blame the Serb. The Serb becomes the party to be
                    sacrificed. Croats are willing to sever relationship with the Serb and paint them
                    as dirty and unapologetic.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Croats start to form a mythology. In this case it&#8217;s the
                    story of Serbian invasion of <span class=SpellE>Vukovar</span>. Note that Serbs
                    did do real violence against Croats, but this is part of what makes them the
                    perfect candidate for sacrifice. They have done something wrong, which seems
                    somewhat related to the problems of the original community, but perhaps they
                    really aren&#8217;t the cause of the Croat&#8217;s problems. The mythology explains how
                    Serbs are dirty, godless, unapologetic, did great evil against us Croatians,
                    and now need to be expelled, diminished, or pushed back against, because they
                    are the cause of our problems. The exclusion takes the surface form of keeping
                    Serbian language out of public life, which is justified by Croats by appealing to
                    the story of <span class=SpellE>Vukovar</span>. Note that their story is that
                    they don&#8217;t recognize any weight to the Serbian president having apologized for
                    Serbia&#8217;s actions against <span class=SpellE>Vukovar</span>, and they don&#8217;t
                    realize their own impure acts against the Serbs in the war.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>We see this blaming in action when Croats lament the
                    memories that Cyrillic brings them. The leader of the anti-Cyrillic movement&#8217;s
                    name is Tomislav <span class=SpellE>Josic</span>. In response to the sign
                    tearing down he told the media that <span style='color:#2D3033'>&quot;Cyrillic
                      letters used to come to <span class=SpellE>Vukovar</span> on army tanks,&#8217; &#8230;
                      referring to the occupation of the town by Serb rebels during the 1991-1995 war&quot;
                      (<span class=SpellE>Kartus</span>). Here we see the attitude of the protestors
                      against Cyrillic as something they say reminds them of the invasion.</span> This
                    anti-Cyrillic sentiment binds together the people against it, as they remember
                    the invasion against them as a clearly defined group of people. <o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Furthermore, we see this victimization claim in the HDZ&#8217;s
                    statements requesting that <span class=SpellE>Vukovar</span> be a place
                    exempted from the minority language law. &quot;The HDZ does not agree with the
                    setting of the Cyrillic alphabet.&nbsp;We ask that the city of <span class=SpellE>Vukovar</span> is
                    a special piety, we ask that the Government has
                    the sensibility and emotion for what is happening in <span class=SpellE>Vukovar</span>&quot;
                    (Dalmacija).<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Croats then get the scapegoat effect, which binds them
                    together. The scapegoat effect is &quot;that strange process through which two
                    or more people are reconciled at the expense of a third party who appears
                    guilty or responsible for whatever ails, disturbs, or frightens the
                    scapegoaters. They feel relieved of their <span class=GramE>tensions</span> and
                    they coalesce into a more harmonious group. They now have a single purpose,
                    which is to prevent the scapegoat from harming them, by expelling and
                    destroying them.&quot; Preventing the Serbs from putting up Cyrillic language
                    gives the nationalistic Croats a sense of unity and order, which they
                    appreciate.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>In attacking the Cyrillic part of the bilingual town hall sign,
                    the Croats were able to act in a unified way against a clearly defined
                    antagonist. <span class=SpellE>Josic</span> stated that <span style='color:
#2D3033'>“Our friends from all over Croatia would come and help us tear down
                      the signs.” </span>The Cyrillic represented the Serbs, who Croats could blame
                    for their own ills. The Croats bring up the Serbs and their invasion as the
                    cause of their own ills. Which was a true event &#8211; the Serbs really did attack
                    and invade. But the true cause of being pulled apart is the Croat war vets&#8217; own
                    inability to attain their object of desire &#8211; pure identity. And as time goes
                    on, this group no longer feels so unified as they once did. Their identity
                    markers are becoming less clearly distinguished from Serbs. If Serbs get the
                    right to official language use, then that&#8217;s one more act of undifferentiating
                    put into place.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>Though attacking the language of Cyrillic may bring the
                    community of veterans back together, and in that way give them the scapegoat
                    effect, one way that this anti-Serbian situation does not fit with Girard&#8217;s
                    theory is that the Serbs are not revealed as sacred after their being
                    &quot;killed&quot;. When I say that the Serbs are killed, I am pointing to the
                    violence the Croats have done in limiting the Serbs. It is not so extreme as to
                    have killed anyone, though there is tension. Perhaps because no one has been
                    killed this is part of the reason why no Serb is praised as divine, <span class=GramE>noble</span>
                    or helpful. Normally the victim that is limited or
                    which has had relationship cut off from the rest of the group is then seen as
                    bringing peace, or somehow appeasing some divine anger. However, if the Serbs
                    would agree to take this position as victims, as the cause of the problem in
                    Croatian vets and apologize for it, then they would be venerated. They would
                    still be limited, but also appreciated as willing to admit their own complicity
                    in the problem. But the distinction in this case is that the Serbs are not
                    willing to accept that because of their invasion against <span class=SpellE>Vukovar</span>
                    20 years ago they should not be able to use their language <span class=SpellE>publically</span>
                    today.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%;mso-outline-level:1'><b
                    style='mso-bidi-font-weight:normal'><span style='mso-bidi-font-family:"Times New Roman"'>Responding
                      to the antagonism with counseling, company, and communication</span></b><span
                    style='mso-bidi-font-family:"Times New Roman"'>
                    <o:p></o:p>
                  </span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>A helpful venue of action would be increasing counseling and
                    legal services, like what <span class=SpellE>B.a.B.e</span>. is doing with
                    abused women. I suggest that they develop programs for men as well, especially
                    for veterans and those with PTSD. Furthermore, an understanding of the cause of
                    antagonism against Serbs proves helpful: Croats are trying to get a pure
                    identity, but can&#8217;t, and in the process are denying hurtful parts of their
                    experience contradict their acquisition of pure identity.<o:p></o:p></span></p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-bidi-font-family:
"Times New Roman"'>As Croats continue with war trauma, which keep them from
                    reaching, and causing them to do violence to each other, and then spilling over
                    into other groups. Bringing attention to the fact in the city council room
                    could be a helpful starting point. I would expect much backlash in pointing out
                    these problems in <span class=SpellE>Vukovar&#8217;s</span> council room. Long term healing
                    will require the Croat community to develop desire a different object, and in
                    this case, that means a willingness to give up the hope of this a pure identity
                    which consists of Croatians as not guilty in the war. The message that people
                    must be willing to accept that they have a dirty past. It was difficult for a
                    few Israeli soldiers to admit their crimes in 1948 (Cook). One said in a truth
                    commission, &quot;At that time I did not see anything wrong with what we were
                    doing,&quot; Neumann said. &quot;If I was told to do things that I do not want
                    to mention [here], I did them with no doubts at all</span><span
                    style='font-family:MingLiU;mso-bidi-font-family:MingLiU'></span><span
                    style='mso-bidi-font-family:"Times New Roman"'>. Not now. It is already 50, 60
                    years that I am filled with regret.&quot; It could be helpful to be around
                    people able to admit their own past faults, such as these Israeli soldiers.
                    Perhaps those soldiers could come visit the town and talk about their past, and
                    the way that they&#8217;ve come to accept the horrible things they did, though they
                    still regret them. It would be better for these people to reach for an identity
                    more focused and faithful to a different part of the past: the former
                    Yugoslavia&#8217;s more unified, multiethnic notion of fraternity.<o:p></o:p></span></p>

