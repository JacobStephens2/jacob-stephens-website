mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[10]</span></span>
                        <![endif]>
                      </span></span></a>
                  There is simply a lack of evidence for the assertion that it is not possible
                  for two natures in one being. “If someone responds that he or she does not
                  understand how Jesus could have two centers of concsiousness and still be one
                  person, then that fact may certainly be admitted by all.”<a style='mso-footnote-id:
ftn11' href="#_ftn11" name="_ftnref11" title=""><span class=MsoFootnoteReference><span
                        style='mso-special-character:footnote'>
                        <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;
