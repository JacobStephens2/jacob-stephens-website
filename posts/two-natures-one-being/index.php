                <p class=MsoNormal style='line-height:200%'><b style='mso-bidi-font-weight:
normal'>What A Church Living According to Bonhoeffer’s Christology Would Look
                    Like<o:p></o:p></b></p>

                <p class=MsoNormal style='line-height:200%'><b style='mso-bidi-font-weight:
normal'><span style='mso-tab-count:1'> </span></b>Bonhoeffer’s
                  beliefs about the person of Christ come to emphasize his being on earth, so the
                  way we see that played out is by examining the ways peoples’ understanding of
                  how Christ works. Instead of an active focus on the ways that God is acting in
                  new ways now across the earth, this church would focus more on the work of
                  Christ as he was on the earth, because his work centered around Christ’s work
                  then. If he doesn’t have human nature now, or at least, if that is not
                  emphasized, we likely won’t be focusing on how people will be acting out Christ’s
                  empowerment now. That’s not true though, because the work of the Holy Spirit
                  through people today is likely still affirmed by Bonhoeffer.</p>

                <p class=MsoNormal style='text-indent:.5in;line-height:200%'>In the end of <i
                    style='mso-bidi-font-style:normal'>Life Together</i> he talks about The Word
                  speaking to people. Bonhoeffer has a high view of how Jesus’ word as recorded
                  in Scripture can affect them. He describes meditation on the Word of God as
                  powerful, and he views Jesus as having spoken in the authority of God. So this
                  community will live according to the Word of God as revealed through Christ
                  while he was on earth.</p>

                <p class=MsoNormal style='line-height:200%'><b style='mso-bidi-font-weight:
normal'>Conclusion</b></p>

                <p class=MsoNormal style='line-height:200%'>Bonhoeffer believes in the full
                  human nature and full divine nature of Christ coexisting in Jesus while he
                  lived on earth. We see this through Bonhoeffer’s language about Jesus being the
                  Son of God and taking on human nature. We see the way that these natures relate
                  by looking at how Jesus dealt with temptation. I agree with Bonhoeffer, and I
                  am left perplexed about whether or not Jesus presently having human nature is
                  important. Also, I am confused about whether Jesus needs to have risen
                  physically from the dead. Why couldn’t he have been right in his teachings and
                  work of dying and reconciling, then simply come to be with God in divine nature
                  again, as he was before, and the reconciliatory, mediative work done with?
                  These are areas I value putting more research into, because I think there is an
                  important reason as to why Jesus had to rise physically, but at the moment I do
                  not know why.<br clear=all style='mso-special-character:line-break;page-break-before:
always'>
                </p>

