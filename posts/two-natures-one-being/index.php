                        <![endif]>
                      </span></span></a>
                  It is important to accept this that we cannot understand. It is an act of
                  humility,<a style='mso-footnote-id:ftn12' href="#_ftn12" name="_ftnref12" title=""><span
                      class=MsoFootnoteReference><span style='mso-special-character:
