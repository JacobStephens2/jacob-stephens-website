mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[12]</span></span>
                        <![endif]>
                      </span></span></a>
                  and we must make a leap to the absurd to have any claims on truth.<a style='mso-footnote-id:ftn13'
                    href="#_ftn13" name="_ftnref13" title=""><span class=MsoFootnoteReference><span
                        style='mso-special-character:footnote'>
