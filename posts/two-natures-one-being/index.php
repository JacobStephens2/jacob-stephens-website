          <article class="single-module--article--1iUKO">
            <h1 class="single-module--article__title--1m8hL">Two Natures, One Being: An Evaluation of Bonhoeffer on the Person of Christ</h1>
            <div class="single-module--article__meta--2B_eP">by <!-- -->Jacob Stephens<!-- -->. Published<!-- -->
              <!-- -->March 17, 2015</div>
            <div class="single-module--article__content--7dEpL">
              <div class=WordSection1>

                <p class=MsoNormal align=center style='text-align:center;line-height:200%'>BCM
                  308 I: Research Paper A</p>

                <p class=MsoNormal align=center style='text-align:center;line-height:200%'>Prof.
                  James Arcadi</p>

                <p class=MsoNormal style='line-height:200%'><b style='mso-bidi-font-weight:
normal'>Introduction<o:p></o:p></b></p>

                <p class=MsoNormal style='line-height:200%'>Who is Jesus? This question is central
                  to Christology. Christology may generally be defined as “the theological study
                  devoted to answering two main questions: Who is Jesus? (the question of
                  identity) and what is the nature and significance of what Jesus accomplished in
                  the incarnation (the question of his work).”<a style='mso-footnote-id:ftn1' href="#_ftn1"
                    name="_ftnref1" title=""><span class=MsoFootnoteReference><span
                        style='mso-special-character:footnote'>
                        <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;
mso-fareast-font-family:"MS Mincho";mso-fareast-theme-font:minor-fareast;
mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[1]</span></span>
                        <![endif]>
                      </span></span></a>
                  We will be focused on his identity. Does Jesus have a fully divine nature and a
                  fully human nature? In a 2015 Gordon College BCM308 Christian Theology class
                  lecture, Dr. Green presented that Christology is “the study of his human and
                  divine natures and how those natures relate to each other.” This presents the
                  further question, how do those natures relate to one another? How can they
                  relate?<i style='mso-bidi-font-style:normal'>
                    <o:p></o:p>
                  </i></p>

                <p class=MsoNormal style='text-indent:.5in;line-height:200%'>This paper will
                  explore the understanding of the Person of Christ Bonhoeffer conveys through looking
                  at <i style='mso-bidi-font-style:normal'>Life Together</i>. Then we will look
                  at a few other sources in coming to understand some of Bonhoeffer’s
                  perspective. Next we will move into exploration of my own understanding, with a
                  portion of that segment comparing these two understandings. Bonhoeffer and I
                  both believe that the Person of Christ has two full natures, one human and one
                  divine, and that those natures interact such that sometimes Christ relies on
                  one and not the other. We disagree on our understanding of what it means for
                  Christ now to be fully human. I believe Christ has a human flesh aspect to him
                  now (restored and ressurected like we will have one day), while Bonhoeffers’
                  belief in the present Christ does not have a human flesh aspect like we will
                  have.</p>

                <p class=MsoNormal style='line-height:200%'><b style='mso-bidi-font-weight:
normal'>The Person of Christ in <i style='mso-bidi-font-style:normal'>Life Together<o:p></o:p></i></b></p>

                <p class=MsoNormal style='line-height:200%'>Bonhoeffer, through <i
                    style='mso-bidi-font-style:normal'>Life Together</i> presents a picture of
                  Christ that affirms his two natures: full humanity as well as full divinity. <i
                    style='mso-bidi-font-style:normal'>Life Together</i> is his exposition on the
                  nature of Christian community, and how it ought to be lived out. He wrote <i
                    style='mso-bidi-font-style:normal'>Life Together </i>during his 1938 stay in
                  Pomerania secretly training young vicars. The life he describes in this book is
                  the life that they shared together. The first chapter focuses on community, and
                  in it Bonhoeffer describes the purpose of Christian community as being
                  ministering to one another in Christ. </p>

                <p class=MsoNormal style='text-indent:.5in;line-height:200%'>We see Bonhoeffer’s
                  belief in Jesus’ fully divine nature and as fully human nature when he writes,
                  “when God’s Son took on flesh, he truly and bodily took on, out of pure grace,
                  our being, our nature, ourselves.”<a style='mso-footnote-id:ftn2' href="#_ftn2" name="_ftnref2"
                    title=""><span class=MsoFootnoteReference><span style='mso-special-character:footnote'>
                        <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;
mso-fareast-font-family:"MS Mincho";mso-fareast-theme-font:minor-fareast;
mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[2]</span></span>
                        <![endif]>
                      </span></span></a><span style='mso-spacerun:yes'> </span>“God’s Son” refers to Jesus, and calling
                  him
                  God’s Son in capital letters signifies that he has divine nature. Jesus “truly
                  … took on … our nature”, meaning that Jesus took on human nature. Jesus in this
                  view has fully human and fully divine nature. He did not lose his divine nature
                  when he became human. </p>

                <p class=MsoNormal style='text-indent:.5in;line-height:200%'>Bonhoeffer’s
                  language, “took on”, signifies that there was a moment in time when he became
                  human. He therefore did not have a human nature for all of eternity, but he at
                  least had it throughout the course of his life on earth.</p>

                <p class=MsoNormal style='text-indent:.5in;line-height:200%'>Bonhoeffer does
                  not affirm that Christ still has a physical human nature. He also does not make
                  statements against Christ presently having a physical human nature. He remains
                  silent on the topic. Though his absence of statement on this topic I am lead to
                  hypothesize that he believes Christ does not need a presently human nature in
                  order to mediate between man and God. Paul’s language in 1 Tim. 2:5 does not necessitate
                  Christ having a presently active role of mediating, which would presently
                  require a human . I imagine Bonhoeffer could have thought that Christ’s.
                  However, I’m in danger now of reading too far into Bonhoeffer’s absence of
                  language, and thus giving him a false image to my reader.</p>

                <p class=MsoNormal style='text-indent:.5in;line-height:200%'>Bonhoeffer does
                  not explicitly affirm a physical aspect to the resurrection of Christ in <i
                    style='mso-bidi-font-style:normal'>Life Together. </i>He refers to resurrection
                  as something which can fill our periods of meditation<a style='mso-footnote-id:
ftn3' href="#_ftn3" name="_ftnref3" title=""><span class=MsoFootnoteReference><span
                        style='mso-special-character:footnote'>
                        <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;
mso-fareast-font-family:"MS Mincho";mso-fareast-theme-font:minor-fareast;
mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[3]</span></span>
                        <![endif]>
                      </span></span></a>,
                  and also describes John of Patmos’ vision of “all the splendor of the
                  resurrection.”<a style='mso-footnote-id:ftn4' href="#_ftn4" name="_ftnref4" title=""><span
                      class=MsoFootnoteReference><span style='mso-special-character:
footnote'>
                        <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;mso-fareast-font-family:
"MS Mincho";mso-fareast-theme-font:minor-fareast;mso-ansi-language:EN-US;
mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[4]</span></span>
                        <![endif]>
                      </span></span></a>
                  Whether or not Bonhoeffer believed Christ historically raised from the dead is
                  not made explicit in <i style='mso-bidi-font-style:normal'>Life Together</i>.
                  Andy Stearns, Th.M., expresses doubt towards the idea that Bonhoeffer believed
                  in a historical resurrection.<span class=MsoFootnoteReference> <a style='mso-footnote-id:ftn5'
                      href="#_ftn5" name="_ftnref5" title=""><span style='mso-special-character:footnote'>
                        <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;
mso-fareast-font-family:"MS Mincho";mso-fareast-theme-font:minor-fareast;
mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[5]</span></span>
                        <![endif]>
                      </span></a></span>
                  Stearns points to a passage in Bonhoeffer’s <i style='mso-bidi-font-style:normal'>The
                    Cost of </i><span style='mso-spacerun:yes'> </span><i style='mso-bidi-font-style:
normal'>Discipleship</i>: “if we take the statement that Christ is risen and
                  present as an ontological proposition, it inevitably dissolves the unity of the
                  scriptures, for it leads us to speak of a mode of Christ’s presence which is
                  different e.g. from that of the synoptic Jesus.”<a style='mso-footnote-id:ftn6' href="#_ftn6"
                    name="_ftnref6" title=""><span class=MsoFootnoteReference><span
                        style='mso-special-character:footnote'>
                        <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;
mso-fareast-font-family:"MS Mincho";mso-fareast-theme-font:minor-fareast;
mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[6]</span></span>
                        <![endif]>
                      </span></span></a>
                  Here Bonhoeffer makes the critique that Christ’s present risenness gives us a
                  present description of Christ’s presence that is different from Jesus as he is presented
                  in the gospels. I’m having a hard time putting words to (conceptualizing?) how
                  Jesus might presently exist if he did not resurrect. I suppose that the
                  consequence is that Jesus might now not have a human nature, or that he might
                  now not have a body but yet somehow have a human nature.</p>

                <p class=MsoNormal style='text-indent:.5in;line-height:200%'>I’m not sure what
                  the ethical implications of putting such great focus into whether or not Jesus
                  presently has a human nature. Perhaps this could affect our beliefs about
                  whether Jesus </p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-tab-count:1'> </span>Jesus’
                  divine nature is affirmed by Bonhoeffer in his statement that “Christ became
                  the Mediator and made peace with God and among men…Christ opened up the way to
                  God and to our brother.”<a style='mso-footnote-id:ftn7' href="#_ftn7" name="_ftnref7" title=""><span
                      class=MsoFootnoteReference><span style='mso-special-character:footnote'>
                        <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;
mso-fareast-font-family:"MS Mincho";mso-fareast-theme-font:minor-fareast;
mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[7]</span></span>
                        <![endif]>
                      </span></span></a></p>

                <p class=MsoNormal style='text-indent:.5in;line-height:200%'>We can extrapolate
                  how Jesus’ two natures relate by examining the implications of Bonhoeffers’ statement
                  about Jesus’ experience with human suffering. “[The] Man Jesus Christ, to whom
                  no affliction, no ill, no suffering is alien and who yet was the wholly
                  innocent and righteous one.”<a style='mso-footnote-id:ftn8' href="#_ftn8" name="_ftnref8"
                    title=""><span class=MsoFootnoteReference><span style='mso-special-character:footnote'>
                        <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;
mso-fareast-font-family:"MS Mincho";mso-fareast-theme-font:minor-fareast;
mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[8]</span></span>
                        <![endif]>
                      </span></span></a>
                  Jesus knew what every suffering was like, and he also was innocent, meaning
                  that he was sinless. There is one suffering though that he did not know, and
                  that was the suffering that comes from having personally sinned. Looking back
                  on that experience can cause suffering. What we can imply Jesus has experienced
                  though according to Bonhoeffer is the experience of suffering that comes from
                  being tempted. This will be relevant later as we extrapolate how Bonhoeffer may
                  have understood how Christ’s human and divine natures relate.</p>

                <p class=MsoNormal style='line-height:200%'><b style='mso-bidi-font-weight:
normal'>The Person of Christ in My Understanding<o:p></o:p></b></p>

                <p class=MsoNormal style='line-height:200%'>I believe that Jesus Christ
                  presently has a fully human and fully divine nature. We have seen that
                  Bonhoeffer understands Christ as presently existing as fully God because we’re
                  assuming Bonhoefffer held a trinitarian view of God, and as such, if Jesus once
                  was divine, he must always be, because he is one of the members of the trinity.
                  If he were ever without his divine nature, God’s nature would have changed. I
                  am taking the position in my interpretation of Bonhoeffer’s text that he did
                  not assert Jesus was fully human as seen through his use of “Word of God”, and
                  description of the resurrection, and by reasoning of omission conclude that he
                  at least did not find Jesus’ present nature, whether it is human or not, as
                  important. This was not critical to his discussion in <i style='mso-bidi-font-style:
normal'>Life Together</i>. We cannot leap to the conclusion that he understood
                  Christ as having lost his human nature, but we can leap to the conclusion that
                  he did not find it significant for a basic understanding of Christian community
                  and how to live it out.</p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-tab-count:1'> </span>So,
                  Bonhoeffer and I agree in our understanding of Christ, at least while he was
                  living on earth, that then he had fully divine and fully human natures. We see
                  that through Bonhoeffer’s description of Christ coming down as the Son of God
                  into someone who fully took on “our nature”. </p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-tab-count:1'> </span>We
                  agree in our opinion of Jesus at a specific time having taken on a human
                  nature. He stated that God “took on”<span style='mso-spacerun:yes'>
                  </span>flesh. And I draw my assertion from the beginning of the gospel of John,
                  verse 1:14, “Word became flesh.” I understand the time sensitive word “became”
                  to be implying that there was a moment when Jesus took on human nature. </p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-tab-count:1'> </span>We
                  likely agree in our understanding of how Jesus’ human nature and divine nature
                  relate. Through examining the way Jesus responded to temptation, we can come to
                  an understanding of Jesus’ natures relations. Bonhoeffer seems to imply that
                  Jesus experienced temptation. In Jesus temptation, Wayne Grudem explains the
                  connection between Jesus’ two natures.<a style='mso-footnote-id:ftn9' href="#_ftn9" name="_ftnref9"
                    title=""><span class=MsoFootnoteReference><span style='mso-special-character:footnote'>
                        <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;
mso-fareast-font-family:"MS Mincho";mso-fareast-theme-font:minor-fareast;
mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[9]</span></span>
                        <![endif]>
                      </span></span></a>
                  <span style='mso-spacerun:yes'> </span>There is the human nature that Jesus has,
                  and in that he can rely on it to resist the temptation, but he need not rely on
                  his divine nature in order to resist temptation. These temptations are not
                  sayings which are required to have divine nature to resist. And I don’t think
                  that it was the Holy Spirit who enabled him to resist, because before he was
                  anointed with the Holy Spirit, he was sinless, so even then somehow he went
                  without giving in to sin. However that works, we still here see some of the
                  relationship between the two natures. They coexist in Jesus but he does not
                  always work according to both. In the temptation narrative he worked according
                  to the human nature, without relying on the divine nature.
                </p>

                <p class=MsoNormal style='line-height:200%'><span style='mso-tab-count:1'> </span>We
                  see that the one human nature and the divine nature are related to each other
                  through some common thing, Jesus’ actions, which can be affected and guided by
                  one or the other. Jesus has two wills, but one person. The divine nature is
                  connected to his person, and the human nature is also connected to it. They are
                  related to one another through both being connected to his person, and they are
                  both part of his identity, and sometimes one is relied in such a way that the
                  other must not be relied on. These natures coexist in the one person of Jesus,
                  but not every quality of both natures is relied on all the time, such as the
                  aspect of his divine nature which means it’s impossible for him to sin. </p>

                <p class=MsoNormal style='text-indent:.5in;line-height:200%'>There is seeming
                  paradox in Jesus having both physical nature and divine nature. These are not
                  actually impossible to coexist.<a style='mso-footnote-id:ftn10' href="#_ftn10" name="_ftnref10"
                    title=""><span class=MsoFootnoteReference><span style='mso-special-character:footnote'>
                        <![if !supportFootnotes]><span class=MsoFootnoteReference><span style='font-size:12.0pt;font-family:"Times New Roman",serif;
