                        <![endif]>
                      </span></span></a>
                  Like we do not know what it’s like to have the nature of a cat, we don’t know
                  what it’s like to have to have divine nature and human nature.<a style='mso-footnote-id:ftn14'
                    href="#_ftn14" name="_ftnref14" title=""><span class=MsoFootnoteReference><span
